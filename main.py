import face_recognition
import cv2
import pickle
import os
import numpy as np
import openpyxl
from openpyxl.styles import PatternFill, Font, Alignment
from datetime import datetime
from google.colab.patches import cv2_imshow

# Load embedding database
with open("/content/drive/MyDrive/FaceAttendance/embedding_database.pkl", "rb") as f:
    data = pickle.load(f)

known_embeddings = data["embeddings"]
known_names = data["names"]

image_folder = "/content/drive/MyDrive/testing_data"

present_students = set()

THRESHOLD = 0.5   # confidence threshold


for image_name in os.listdir(image_folder):

    image_path = os.path.join(image_folder, image_name)

    print("\nProcessing image:", image_name)

    image = cv2.imread(image_path)

    rgb_image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)

    # Detect faces
    face_locations = face_recognition.face_locations(rgb_image, model="cnn")

    # Generate embeddings
    face_encodings = face_recognition.face_encodings(rgb_image, face_locations)

    print("Faces detected:", len(face_locations))


    for (top, right, bottom, left), encoding in zip(face_locations, face_encodings):

        # Compare with database
        face_distances = face_recognition.face_distance(known_embeddings, encoding)

        best_match_index = np.argmin(face_distances)

        distance = face_distances[best_match_index]

        if distance < THRESHOLD:
            name = known_names[best_match_index]
            present_students.add(name)
        else:
            name = "Unknown"

        print("Predicted:", name, "| Distance:", round(distance,3))


        # Draw bounding box
        cv2.rectangle(image, (left, top), (right, bottom), (0,255,0), 2)

        # Draw name
        cv2.putText(
            image,
            name,
            (left, top - 10),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.8,
            (0,255,0),
            2
        )

    # Show annotated image
    cv2_imshow(image)


print("\n========================")
print("Students Present:")
print("========================")

for student in sorted(present_students):
    print(student)


# ── Write attendance to Excel ─────────────────────────────────────────────────

excel_db_path    = "/content/drive/MyDrive/FaceAttendance/student_database.xlsx"
attendance_path  = "/content/drive/MyDrive/FaceAttendance/attendance.xlsx"

# Load master student list
db_wb = openpyxl.load_workbook(excel_db_path)
db_ws = db_wb["Students"]
all_students = {}
for row in db_ws.iter_rows(min_row=2, values_only=True):
    sno, sid, display, folder = row
    if folder:
        all_students[folder] = {"id": sid, "display": display}

# Load or create attendance workbook
if os.path.exists(attendance_path):
    att_wb = openpyxl.load_workbook(attendance_path)
else:
    att_wb = openpyxl.Workbook()
    if "Sheet" in att_wb.sheetnames:
        del att_wb["Sheet"]

# New sheet named by today's date
today_str  = datetime.now().strftime("%Y-%m-%d")
sheet_name = today_str
counter    = 1
while sheet_name in att_wb.sheetnames:
    sheet_name = f"{today_str}_run{counter}"
    counter   += 1

att_ws = att_wb.create_sheet(title=sheet_name)

hdr_fill     = PatternFill("solid", fgColor="1F4E79")
hdr_font     = Font(bold=True, color="FFFFFF", size=11)
present_fill = PatternFill("solid", fgColor="C6EFCE")
absent_fill  = PatternFill("solid", fgColor="FFC7CE")
center       = Alignment(horizontal="center")

for col, h in enumerate(["S.No", "Student ID", "Name", "Attendance"], 1):
    c = att_ws.cell(row=1, column=col, value=h)
    c.fill = hdr_fill; c.font = hdr_font; c.alignment = center

att_ws.column_dimensions['A'].width = 8
att_ws.column_dimensions['B'].width = 12
att_ws.column_dimensions['C'].width = 28
att_ws.column_dimensions['D'].width = 14

row_num = 2
present_count = 0
for folder_name, info in sorted(all_students.items()):
    is_present = folder_name in present_students
    status     = "Present" if is_present else "Absent"
    fill       = present_fill if is_present else absent_fill
    if is_present:
        present_count += 1
    att_ws.cell(row=row_num, column=1, value=row_num - 1).alignment = center
    att_ws.cell(row=row_num, column=2, value=info["id"]).alignment  = center
    att_ws.cell(row=row_num, column=3, value=info["display"])
    sc = att_ws.cell(row=row_num, column=4, value=status)
    sc.fill = fill; sc.alignment = center; sc.font = Font(bold=True)
    row_num += 1

att_ws.append([])
total = len(all_students)
att_ws.append([None, None, "Total Present",  present_count])
att_ws.append([None, None, "Total Absent",   total - present_count])
att_ws.append([None, None, "Total Students", total])

att_wb.save(attendance_path)

print("\n========================")
print("Attendance saved to Excel!")
print("========================")
print(f"Sheet: {sheet_name}")
print(f"Present: {present_count} / {total}")
print(f"Absent:  {total - present_count} / {total}")