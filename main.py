# Dataset Preparation (To generate frames from the given videos of the students)
import cv2
import face_recognition
import os

video_folder = "/content/drive/MyDrive/face_dataset"
dataset_path = "/content/drive/MyDrive/FaceAttendance/dataset"

os.makedirs(dataset_path, exist_ok=True)

videos = os.listdir(video_folder)

for video_file in videos:

    video_path = os.path.join(video_folder, video_file)

    name = os.path.splitext(video_file)[0]
    name = name.title()
    folder_name = name.replace(" ", "_")

    student_folder = os.path.join(dataset_path, folder_name)
    os.makedirs(student_folder, exist_ok=True)

    # Skip if already processed
    if len(os.listdir(student_folder)) > 15:
        print(folder_name, "already done")
        continue

    cap = cv2.VideoCapture(video_path)

    frame_total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    step = 15   # jump 15 frames at a time

    saved_count = 0

    for frame_number in range(0, frame_total, step):

        cap.set(cv2.CAP_PROP_POS_FRAMES, frame_number)
        ret, frame = cap.read()

        if not ret:
            break

        rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)

        faces = face_recognition.face_locations(rgb, model="hog")

        for top, right, bottom, left in faces:

            face = frame[top:bottom, left:right]

            filename = os.path.join(
                student_folder,
                f"{folder_name}_{saved_count}.jpg"
            )

            cv2.imwrite(filename, face)
            saved_count += 1

    cap.release()

    print(folder_name, "→", saved_count, "faces saved")

